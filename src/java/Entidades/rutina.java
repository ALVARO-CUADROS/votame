public class rutina {
    public static String id;
    public static String fecha;
    public static String dur;
    public static String regu;
    public static String nseries;
    public static String dista;
    public static String velo;
    public static String pode;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getDur() {
        return dur;
    }

    public void setDur(String dur) {
        this.dur = dur;
    }

    public String getRegu() {
        return regu;
    }

    public void setRegu(String regu) {
        this.regu = regu;
    }

    public String getNseries() {
        return nseries;
    }

    public void setNseries(String nseries) {
        this.nseries = nseries;
    }

    public String getDista() {
        return dista;
    }

    public void setDista(String dista) {
        this.dista = dista;
    }

    public String getVelo() {
        return velo;
    }

    public void setVelo(String velo) {
        this.velo = velo;
    }

    public String getPode() {
        return pode;
    }

    public void setPode(String pode) {
        this.pode = pode;
    }
    
    
}
